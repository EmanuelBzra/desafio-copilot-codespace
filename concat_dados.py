# Vamos receber dois dados diferentes do usuário e concatena-los em uma unica string

# Recebe as duas entradas do usuário
entrada1 = input("Digite a primeira entrada: ")
entrada2 = input("Digite a segunda entrada: ")

# Concatena as duas entradas em uma única string
resultado = entrada1 + " " + entrada2

# Exibe o resultado
print("A string concatenada é:", resultado)
